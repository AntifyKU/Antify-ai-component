"""
Prepare dataset for YOLOv8 classification training.
Organizes ant images into train/val/test splits for binary classification.

Usage:
    python prepare_yolo_dataset.py [--output datasets/ant_classifier] [--train-ratio 0.7] [--val-ratio 0.2]
"""

import os
import shutil
import random
import argparse
from pathlib import Path
from tqdm import tqdm
import csv


def prepare_classification_dataset(
    images_dir: str,
    labels_csv: str,
    output_dir: str,
    train_ratio: float = 0.7,
    val_ratio: float = 0.2,
    seed: int = 42,
    dry_run: bool = False
):
    """
    Prepare dataset for YOLOv8 classification (ant detection).
    
    Structure created:
        output_dir/
            train/
                ant/
                    image1.jpg
                    ...
            val/
                ant/
                    ...
            test/
                ant/
                    ...
    """
    random.seed(seed)
    
    output_path = Path(output_dir)
    images_path = Path(images_dir)
    
    # Collect all image paths from labels CSV
    image_files = []
    
    print(f"Reading labels from {labels_csv}...")
    with open(labels_csv, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            img_path = images_path.parent / row['image_path'].replace('\\', '/')
            if img_path.exists():
                image_files.append(img_path)
    
    print(f"Found {len(image_files)} valid images")
    
    if len(image_files) == 0:
        print("No images found! Check your paths.")
        return
    
    # Shuffle and split
    random.shuffle(image_files)
    
    n_train = int(len(image_files) * train_ratio)
    n_val = int(len(image_files) * val_ratio)
    
    train_files = image_files[:n_train]
    val_files = image_files[n_train:n_train + n_val]
    test_files = image_files[n_train + n_val:]
    
    print(f"\nDataset split:")
    print(f"  Train: {len(train_files)} images ({train_ratio*100:.0f}%)")
    print(f"  Val:   {len(val_files)} images ({val_ratio*100:.0f}%)")
    print(f"  Test:  {len(test_files)} images ({(1-train_ratio-val_ratio)*100:.0f}%)")
    
    if dry_run:
        print("\n[DRY RUN] No files were copied.")
        return
    
    # Create directories
    splits = {
        'train': train_files,
        'val': val_files,
        'test': test_files
    }
    
    for split_name, files in splits.items():
        split_dir = output_path / split_name / 'ant'
        split_dir.mkdir(parents=True, exist_ok=True)
        
        print(f"\nCopying {split_name} images...")
        for img_path in tqdm(files, desc=split_name):
            # Use unique filename to avoid collisions
            new_name = f"{img_path.parent.name}_{img_path.name}"
            dest = split_dir / new_name
            shutil.copy2(img_path, dest)
    
    # Create dataset.yaml for YOLOv8
    yaml_content = f"""# YOLOv8 Ant Classification Dataset
# Auto-generated by prepare_yolo_dataset.py

path: {output_path.absolute()}
train: train
val: val
test: test

# Classes
names:
  0: ant

# Dataset info
nc: 1  # number of classes
"""
    
    yaml_path = output_path / 'dataset.yaml'
    with open(yaml_path, 'w') as f:
        f.write(yaml_content)
    
    print(f"\n✓ Dataset prepared at: {output_path.absolute()}")
    print(f"✓ Config saved to: {yaml_path}")
    print(f"\nNext step: Run training with:")
    print(f"  python train_yolo_detector.py --data {yaml_path}")


def main():
    parser = argparse.ArgumentParser(description='Prepare dataset for YOLOv8 ant classification')
    parser.add_argument('--images', type=str, default='images',
                        help='Path to images directory')
    parser.add_argument('--labels', type=str, default='labels.csv',
                        help='Path to labels CSV file')
    parser.add_argument('--output', type=str, default='datasets/ant_classifier',
                        help='Output directory for prepared dataset')
    parser.add_argument('--train-ratio', type=float, default=0.7,
                        help='Ratio of training data (default: 0.7)')
    parser.add_argument('--val-ratio', type=float, default=0.2,
                        help='Ratio of validation data (default: 0.2)')
    parser.add_argument('--seed', type=int, default=42,
                        help='Random seed for reproducibility')
    parser.add_argument('--dry-run', action='store_true',
                        help='Show what would be done without copying files')
    
    args = parser.parse_args()
    
    prepare_classification_dataset(
        images_dir=args.images,
        labels_csv=args.labels,
        output_dir=args.output,
        train_ratio=args.train_ratio,
        val_ratio=args.val_ratio,
        seed=args.seed,
        dry_run=args.dry_run
    )


if __name__ == '__main__':
    main()
